type keyVal struct {
	k []byte
	v uint32
}
type sorter []keyVal
func (a sorter) Len() int           { return len(a) }
func (a sorter) Swap(i, j int)      { a[i], a[j] = a[j], a[i] }
func (a sorter) Less(i, j int) bool { return a[i].v > a[j].v } // descending order

type keywordsStruct struct {
	km binsearch.CounterBytes
}

func New() *keywordsStruct {
	k := new(keywordsStruct)
	return k
}

func (k *keywordsStruct) Monogram(b []byte) {
	var err error
	var d []byte
	var ok bool
	wordfn := func(word []byte) {
		if d, err = deaccent.Bytes(word); err == nil {
			if len(d) >= 3 {
				if _, ok = stopwords.Find(d); !ok {
					k.km.Add(word, 1)
				}
			}
		}
	}
	tokenize.AllInOne(b, wordfn, true, false, true, true, false)
}

func (k *keywordsStruct) Bigram(b []byte) {
	var err error
	var d []byte
	var ok, last bool
	var lastword bytes.Buffer
	wordfn := func(word []byte) {
		if d, err = deaccent.Bytes(word); err == nil {
			if len(d) >= 3 {
				if _, ok = stopwords.Find(d); !ok {
					if last {
						lastword.WriteByte(' ')
						lastword.Write(word)
						k.km.Add(lastword.Bytes(), 1)
					}
					lastword.Reset()
					lastword.Write(word)
					last = true
				} else {
					last = false
				}
			} else {
				last = false
			}
		} else {
			last = false
		}
	}
	tokenize.AllInOne(b, wordfn, true, false, true, true, false)
}

