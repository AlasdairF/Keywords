func (k *keywordsStruct) Bytes(num int) [][]byte {
	k.km.Build()
	lst := make(sorter, k.km.Len())
	var i, v int
	var word []byte
	k.km.Reset()
	for eof := false; !eof; {
		word, v, eof = k.km.Next()
		lst[i] = keyVal{word, v}
		i++
	}
	sort.Sort(lst)
	if num > i {
		num = i
	}
	res := make([][]byte, num)
	for i=0; i<num; i++ {
		res[i] = lst[i].k
	}
	return res
}

